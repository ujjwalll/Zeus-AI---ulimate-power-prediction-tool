//------------  creating function that runs when direction button is clicked -------------------

function displayDirections(start,destination){
	// CLEAR PREVIOUS DRAWN PATH
	for(layer in map._layers){
		if(layer>94){
		// deletes last added polyline
		map.removeLayer(map._layers[layer]);
		}
	}
	

	if (start == "Basketball Court" || start == "Tennis Court") {
		start="Court";
	}
	if (destination == "Basketball Court" || destination == "Tennis Court") {
		destination="Court";
	}


	var start_section = "";
	var destination_section = "";
	// determining section of start
	if (Section_A.includes(start)) {
		start_section = "Section_A";
	}
	else if (Section_B.includes(start)) {
		start_section = "Section_B";
	}
	else if (Section_C.includes(start)) {
		start_section = "Section_C";
	}
	// determining section of destination
	if (Section_A.includes(destination)) {
		destination_section = "Section_A";
	}
	else if (Section_B.includes(destination)) {
		destination_section = "Section_B";
	}
	else if (Section_C.includes(destination)) {
		destination_section = "Section_C";
	}

	// console.log(start_section,destination_section);

	// updating ans

	if (start=="Gate #2" || destination=="Gate #2") {
		
		if(start=="Faculty Residence 2" || destination=="Faculty Residence 2") {
			L.polyline(directions["Section_C"]["Gate #2"+"to"+"Faculty Residence 2"]).addTo(map);
		}

		else {
			if (start=="Gate #2") {
				start="Faculty Residence 1";
			}
			else {
				destination="Faculty Residence 1";
			}

			if (start_section == destination_section) {
				if ((start + "to" + destination) in directions[start_section]){
					L.polyline([directions[start_section][start + "to" + destination],directions["Section_C"]["Gate #2"+"to"+"Faculty Residence 1"]]).addTo(map);
				}
				else {
					L.polyline([directions[start_section][destination + "to" + start],directions["Section_C"]["Gate #2"+"to"+"Faculty Residence 1"]]).addTo(map);
				}
			}
			else {
				L.polyline([directions[start_section]["mid" + "to" + start],directions["Section_C"]["Gate #2"+"to"+"Faculty Residence 1"],directions[destination_section]["mid" + "to" + destination]]).addTo(map);
			}
		}

	}

	else if (start=="Faculty Residence 2" || destination=="Faculty Residence 2") {

		if (start=="Faculty Residence 2") {
			start="Faculty Residence 1";
		}
		else {
			destination="Faculty Residence 1";
		}



		if (start_section == destination_section) {
			if ((start + "to" + destination) in directions[start_section]){
				L.polyline([directions[start_section][start + "to" + destination],directions["Section_C"]["Faculty Residence 2"+"to"+"Faculty Residence 1"]]).addTo(map);
			}
			else {
				L.polyline([directions[start_section][destination + "to" + start],directions["Section_C"]["Faculty Residence 2"+"to"+"Faculty Residence 1"]]).addTo(map);
			}
		}
		else {
			L.polyline([directions[start_section]["mid" + "to" + start],directions["Section_C"]["Faculty Residence 2"+"to"+"Faculty Residence 1"],directions[destination_section]["mid" + "to" + destination]]).addTo(map);
		}
	}

	else {
		if (start_section == destination_section) {
			if ((start + "to" + destination) in directions[start_section]){
				L.polyline(directions[start_section][start + "to" + destination]).addTo(map);
			}
			else {
				L.polyline(directions[start_section][destination + "to" + start]).addTo(map);
			}
		}
		else {
			L.polyline([directions[start_section]["mid" + "to" + start],directions[destination_section]["mid" + "to" + destination]]).addTo(map);
		}
	}
}
